"use strict";
// Khai báo 3 biến với kiểu dữ liệu number
let number1;
let number2;
let result;
// Gán giá trị cho hai biến đầu tiên
number1 = 4;
number2 = 6;
// Thực hiện phép cộng và gán kết quả cho biến result
result = number1 + number2;
console.log("Kết quả của phép cộng:", result);
// Thực hiện phép trừ và gán kết quả cho biến result
result = number1 - number2;
console.log("Kết quả của phép trừ:", result);
// Thực hiện phép nhân và gán kết quả cho biến result
result = number1 * number2;
console.log("Kết quả của phép nhân:", result);
// Thực hiện phép chia và gán kết quả cho biến result
result = number1 / number2;
console.log("Kết quả của phép chia:", result);
