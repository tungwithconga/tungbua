// Union Types:
// Ý nghĩa: Union Types cho phép một biến có thể giữ một trong nhiều kiểu dữ liệu khác nhau.
// Sử dụng: Được sử dụng khi một biến có thể chứa dữ liệu của nhiều kiểu khác nhau.

// Intersection Types:
// Ý nghĩa: Intersection Types cho phép kết hợp các tính chất của nhiều kiểu dữ liệu thành một kiểu mới.
// Sử dụng: Được sử dụng khi một biến hoặc đối tượng cần chứa tất cả các tính chất từ nhiều kiểu khác nhau.